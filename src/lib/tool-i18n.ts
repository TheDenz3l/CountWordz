import type { Locale } from './i18n';

const SUPPORTED_LOCALES: Locale[] = ['en', 'es', 'fr', 'de', 'pt'];

export function resolveLocale(locale?: string): Locale {
  if (locale && SUPPORTED_LOCALES.includes(locale as Locale)) {
    return locale as Locale;
  }

  return 'en';
}

