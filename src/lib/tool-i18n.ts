export function getToolTranslation<K extends keyof typeof translations.en>(locale: string | undefined, key: K) {
  const resolved = resolveLocale(locale);
  return translations[resolved][key];
}

export function getLocalizedReadabilityBand(score: number, locale?: string) {
  const bands = getToolTranslation(locale, 'readability').bands;

  if (score >= 90) return bands.veryEasy;
  if (score >= 80) return bands.easy;
  if (score >= 70) return bands.fairlyEasy;
  if (score >= 60) return bands.moderate;
  if (score >= 50) return bands.fairlyDifficult;
  if (score >= 30) return bands.difficult;

  return bands.veryDifficult;
}

